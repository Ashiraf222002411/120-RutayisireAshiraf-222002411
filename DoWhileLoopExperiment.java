// defining public class called DoWhileExperiment
public class DoWhileLoopExperiment {
  // creating main method
  public static void main(String[] args) {
    int count = 1; // declaration and initialization
    // setting do while loop
    do {
      System.out.println("Count: " + count);  // printing  the "value count= 1"
      count++; // icrememnting
    } while (count <= 5);
  }
}