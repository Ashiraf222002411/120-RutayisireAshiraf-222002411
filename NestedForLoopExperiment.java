// defining public class called NestedForLoopExperiment
public class NestedForLoopExperiment {
 // creating main method
  public static void main(String[] args) {
    // initializing for loop 
    for (int i = 1; i <= 3; i++) {
      for (int j = 1; j <= 3; j++) {
        System.out.println("i: " + i + ", j: " + j);  // printing the "value of i = 1"
      }
    }
  }
}
