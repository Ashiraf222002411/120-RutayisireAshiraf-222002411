// Define the class named WhileLoopExperiment
public class WhileLoopExperiment2 {
  // Main method, the entry point of the program
  public static void main(String[] args) {
    int count2 = 5; // Declaration and initialization
    // Start the while loop with the condition count <= 5
while (count2 >= 3) {
    System.out.println("Experiment 2: Count2 = " + count2);  // Print the value of count along with the string "Count:"
    count2--;
}
  }
}