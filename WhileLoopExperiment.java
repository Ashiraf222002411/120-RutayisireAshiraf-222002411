// Define the class named WhileLoopExperiment
public class WhileLoopExperiment {
  // Main method, the entry point of the program
  public static void main(String[] args) {
    int count = 1; // Declaration and initialization
    // Start the while loop with the condition count <= 5
    while (count <= 5) {
      System.out.println("Count: " + count); // Print the value of count along with the string "Count:"
      count++; // Increment the value of count by 1
    }
  }
}